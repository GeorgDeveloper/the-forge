/**
 * Интерфейс, представляющий модель пользователя в системе.
 * Используется для типизации данных о пользователях.
 */
export interface IUser {
  /**
   * Уникальный идентификатор пользователя.
   * @type {number}
   * @example 42
   */
  id: number;

  /**
   * Логин пользователя для входа в систему.
   * - Может быть строкой (активный пользователь)
   * - Может быть null (если пользователь удален)
   * - Может быть undefined (если поле не задано)
   * @type {string | null | undefined}
   * @example 'admin', null, undefined
   */
  login?: string | null;
}
